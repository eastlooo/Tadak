import UIKit.UIColor

extension UIColor {
    class var customLightGray: UIColor? { return UIColor(named: "LightGray") }
    class var customLightNavy: UIColor? { return UIColor(named: "LightNavy") }
    class var customNavy: UIColor? { return UIColor(named: "Navy") }
    class var customDarkNavy: UIColor? { return UIColor(named: "DarkNavy") }
    class var customCoral: UIColor? { return UIColor(named: "Coral") }
    class var customPumpkin: UIColor? { return UIColor(named: "Pumpkin") }
    class var customSkyBlue: UIColor? { return UIColor(named: "SkyBlue") }
    class var customDarkCloud: UIColor? { return UIColor(named: "DarkCloud") }
}
